namespace ExtendedDialogBox.Enum
{
    public enum Focused
    {
        /// <summary>
        /// Reset default focus
        /// </summary>
        None,

        /// <summary>
        /// Set focus to OK button
        /// </summary>
        OkButton,

        /// <summary>
        /// Set focus to Yes button
        /// </summary>
        YesButton,

        /// <summary>
        /// Set focus to No button
        /// </summary>
        NoButton,

        /// <summary>
        /// Set focus to Cancel button
        /// </summary>
        CancelButton
    }
}
